package videojuego.ingesoft.utp;

public class Arma {
	private int codArma;
	private int agilidad;
	private int fuerza;
	private int inteligencia;
	private int durabilidad;
	private int equipada;

	public int getCodArma() {
		return codArma;
	}

	public void setCodArma(int codArma) {
		this.codArma = codArma;
	}

	public int getAgilidad() {
		return agilidad;
	}

	public void setAgilidad(int agilidad) {
		this.agilidad = agilidad;
	}

	public int getFuerza() {
		return fuerza;
	}

	public void setFuerza(int fuerza) {
		this.fuerza = fuerza;
	}

	public int getInteligencia() {
		return inteligencia;
	}

	public void setInteligencia(int inteligencia) {
		this.inteligencia = inteligencia;
	}

	public int getDurabilidad() {
		return durabilidad;
	}

	public void setDurabilidad(int durabilidad) {
		this.durabilidad = durabilidad;
	}

	public int getEquipada() {
		return equipada;
	}

	public void setEquipada(int equipada) {
		this.equipada = equipada;
	}

}
